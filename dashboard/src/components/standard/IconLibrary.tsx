export type IconSize = 'xs' | 'sm' | 'md' | 'lg' | 'xl'

const iconSizes: Record<IconSize, string> = {
  xs: 'h-4 w-4',   // 16px
  sm: 'h-5 w-5',   // 20px
  md: 'h-6 w-6',   // 24px
  lg: 'h-8 w-8',   // 32px
  xl: 'h-12 w-12', // 48px
}

// ============================================================================
// ICON WRAPPER COMPONENT
// ============================================================================

interface IconProps {
  name: 'instagram' | 'tiktok' | 'onlyfans' | 'youtube' | 'x' | 'threads' | 'reddit'
  size?: IconSize
  className?: string
  color?: string
}

export const SocialIcon: React.FC<IconProps> = ({
  name,
  size = 'md',
  className,
  color: _color
}) => {
  const sizeClass = iconSizes[size]
  const combinedClass = cn(sizeClass, className)

  const icons = {
    instagram: InstagramIcon,
    tiktok: TikTokIcon,
    onlyfans: OnlyFansIcon,
    youtube: YouTubeIcon,
    x: XTwitterIcon,
    threads: ThreadsIcon,
    reddit: RedditIcon
  }

  const Icon = icons[name]
  if (!Icon) return null

  return <Icon className={combinedClass} />
}

// ============================================================================
// ICON WITH BACKGROUND
// ============================================================================

interface IconWithBgProps extends IconProps {
  bgColor?: string
  bgClassName?: string
  rounded?: 'sm' | 'md' | 'lg' | 'full'
}

export const IconWithBackground: React.FC<IconWithBgProps> = ({
  name,
  size = 'md',
  className,
  bgColor,
  bgClassName,
  rounded = 'lg',
  color
}) => {
  const roundedClasses = {
    sm: 'rounded',
    md: 'rounded-lg',
    lg: 'rounded-xl',
    full: 'rounded-full'
  }

  const paddingBySize = {
    xs: 'p-2',
    sm: 'p-2.5',
    md: 'p-3',
    lg: 'p-4',
    xl: 'p-5'
  }

  return (
    <div className={cn(
      paddingBySize[size],
      roundedClasses[rounded],
      bgClassName || bgColor || 'bg-gray-100',
      'inline-flex items-center justify-center'
    )}>
      <SocialIcon
        name={name}
        size={size}
        className={className}
        color={color}
      />
    </div>
  )
}